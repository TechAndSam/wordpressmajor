            $(function() {
                var serviceStepOneFormValue,serviceStepTwoFormValue;

                $('.order-btn-link').click(function(e) {
                    e.preventDefault();
                    $('#launch-service-modal').click();
                })
                $('.service-list input.checkbox').change(function() { 
                    let __self = $(this);                
                    if (__self.prop('checked')) {                        
                        __self.parents('.form-group').find('.checkboxx').find('span').css({
                            backgroundColor : 'green'
                        },'slow').html("<i class='fa fa-check'></i>");
                        $('#service-total-price').text(Number($('#service-total-price').text()) + Number(__self.attr('service-price')))
                    }else{
                        __self.parents('.form-group').find('.checkboxx').find('span').css({
                            backgroundColor  :  '#0a87ec'
                        },'slow').html("");
                        $('#service-total-price').text(Number($('#service-total-price').text()) - Number(__self.attr('service-price')))
                    }
                })
                $('.checkboxx').each(function() {
                    $(this).click(function() {
                        $('.service-list input[value="'+$(this).attr('data-input')+'"').click();
                    })
                });

                // Submit buttons
                $('#submit-step-one').click(function(e) {
                    e.preventDefault();
                    serviceStepOneFormValue = $('#service-step-1').serialize();
                    if (serviceStepOneFormValue.trim() == '') {
                        alert("Please select the services you would want us to render to you.");
                        return;
                    }                    
                    $(this).attr('disabled','disabled').text("Processing");
                    $('#service-step-1').fadeOut('slow',function () {
                        // $('.service-list').animate({width: '80%'},'fast')                        
                        $('#step-1-heading').fadeOut('fast',function() {
                            $('#submit-step-one').hide();
                            $('.close-step-one').hide();
                            
                            $('#submit-step-two').show();
                            $('.back-to-step-1').show();

                            $('#step-2-heading').fadeIn('slow');
                        });
                        $('#price-container').fadeOut();
                        $('#service-step-2').fadeIn('slow');
                    })
                    $(this).removeAttr('disabled').text("Submit");
                });
                $('#submit-step-two').click(function(e) {
                    e.preventDefault();
                    let __self = $(this);
                    $(this).attr('disabled','disabled').text("Processing");
                    if ($('#email-address').val() == '') {
                        alert("Please fill in your email address");
                        __self.removeAttr('disabled').text("Submit");
                    }else{
                        serviceStepTwoFormValue = $('#service-step-2').serialize();
                    $.ajax({
                        url: 'check-user-account.php',
                        type: 'GET',
                        dataType: 'JSON',
                        data: serviceStepTwoFormValue,
                        success: function (response) {
                            $('#submit-step-two').hide();
                            $('.back-to-step-1').hide();
                            $('.back-to-step-2').show();

                            if (response.isFound == true) {
                                $('#customer-name').text(response.fullname);
                                // show password box
                                $('#service-step-2').fadeOut('slow',function () {                        
                                    $('#step-2-heading').fadeOut('fast',function () {
                                        $('#step-3-heading').fadeIn('slow');
                            
                            
                            $('#submit-step-three').show();
                            
                            $('#submit-step-four').hide();
                                    });
                                    $('#service-step-3').fadeIn();
                                })
                            }else{
                                // show registration form
                                $('#service-step-2').fadeOut('slow',function () {                        
                                    $('#step-2-heading').fadeOut('fast',function () {
                                        $('#step-4-heading').fadeIn('slow');
                                    });
                                    $('#service-step-4').fadeIn();                                    
                                    $('#submit-step-three').hide();
                            $('#submit-step-four').show();
                                })
                            }
                            __self.removeAttr('disabled').text("Submit");
                        },error: function(error) {
                            alert("Opps! The server has encountered a critical error. Please refersh the page and try again.");
                            __self.removeAttr('disabled').text("Submit");
                            console.log(error);
                            
                        }
                    })
                    }
                    
                    
                });


                
                $('#submit-step-three').click(function(e) {
                    e.preventDefault();
                    let __self = $(this);
                    $(this).attr('disabled','disabled').text("Processing");
                    if ($('#email-address').val() == '') {
                        alert("Please fill in your account password");
                        __self.removeAttr('disabled').text("Submit");
                    }else{
                        serviceStepThreeFormValue = $('#service-step-3').serialize() +"&"+ serviceStepTwoFormValue;
                        
                        
                    $.ajax({
                        url: 'check-user-account-password.php',
                        type: 'GET',
                        dataType: 'JSON',
                        data: serviceStepThreeFormValue,
                        success: function (response) {
                            if (response.isTrue == true) {
                                location.href = `order.php?_ref=existing&customer_id=${response.customerId}&___&${serviceStepTwoFormValue}&___&${serviceStepOneFormValue}`;
                            }else{
                                alert("Incorrect account password!");
                            }
                            __self.removeAttr('disabled').text("Submit");
                        },error: function(error) {
                            alert("Opps! The server has encountered a critical error. Please refersh the page and try again.");
                            __self.removeAttr('disabled').text("Submit");
                            console.log(error);
                            
                        }
                    })
                    }
                    
                    
                });

                $('#submit-step-four').click(function(e) {
                    e.preventDefault(); 
                    $(this).attr('disabled','disabled').text("Processing");                   
                    if ($('#ncp').val() !== $('#ncpc').val()) {
                        alert("Password and confirm password not match.")
                    }
                    else{
                        let allFieldsAreFilled = true;
                        $('#service-step-4 input').each(function() {
                            if ($(this).val().trim() == '' && allFieldsAreFilled === true) {
                                allFieldsAreFilled = false;
                                alert('Please fill all fields correctly.');
                                return;
                            }
                        });
                        if (allFieldsAreFilled) {
                            $(this).attr('disabled','disabled').text("Processing");
                            serviceStepFourFormValue = $('#service-step-4').serialize();
                            
                            location.href = `order.php?_ref=new&${serviceStepFourFormValue}&___&${serviceStepTwoFormValue}&___&${serviceStepOneFormValue}`;
                        }
                        
                    }           
                    $(this).removeAttr('disabled').text("Submit");
                });
                // Back button
                $('.back-to-step-1').click(function(e) {
                    e.preventDefault();
                    $('#service-step-2').fadeOut('slow',function () {
                        // $('.service-list').animate({width: '90%'},'fast')                      
                        $('#step-2-heading').fadeOut('fast',function() {
                            $('#step-1-heading').fadeIn('slow');
                            $('#price-container').fadeIn();

                            
                            $('#submit-step-two').hide();
                            $('#submit-step-one').show();
                            $('.close-step-one').show();
                            $('.back-to-step-1').hide();
                        });                        
                        $('#service-step-1').fadeIn('slow');
                    })
                });
                $('.back-to-step-2').click(function(e) {
                    e.preventDefault();
                    $('#service-step-3, #service-step-4').fadeOut('slow',function () {                        
                        $('#step-3-heading, #step-4-heading').fadeOut('fast',function () {
                            $('#step-2-heading').fadeIn('slow');
                            $('#service-step-2').fadeIn('slow');
                            
                            $('#submit-step-three, #submit-step-four').hide();
                            $('#submit-step-two').show();
                            $('.back-to-step-2').hide();                            
                            $('.back-to-step-1').show();
                        });
                        
                        
                    })
                });
                $('.back-to-step-3').click(function(e) {
                    e.preventDefault();
                    $('#service-step-4').fadeOut('slow',function () {                        
                        $('#step-4-heading').fadeOut('fast',function () {
                            $('#step-3-heading').fadeIn('slow');
                            $('#service-step-3').fadeIn('slow');
                            $('#submit-step-four').hide();
                            $('#submit-step-three').show();
                            $('.back-to-step-3').hide();
                        });                      
                        
                    })
                });
            })
